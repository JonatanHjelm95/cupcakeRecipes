package logic;

/**
 *
 * @author Fen
 */
class Ingredient {
    private String name;
    private double qty;

    public Ingredient(String name, double qty) {
        this.name = name;
        this.qty = qty;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public double getQty() {
        return qty;
    }

    public void setQty(double qty) {
        this.qty = qty;
    }
    
    
    
    
}
